# Create a tuple called 'my_task'
my_task = ('Make Dinner', 'Tomorrow', 2, False)

# Print the tuple
print(my_task)

# Accessing the tuple by index
print(my_task[2])

# Using the appropriate method, print the number of objects in the tuple
print(len(my_task))

# Get the index of the object in the tuple with the value 'False' and print it out
index = my_task.index(False)
print(my_task[index])
